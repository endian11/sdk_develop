package com.travelrely.v2.model;

public class FemtoInfo
{
    String mcc;
    String ip;
    int port;
    public String getMcc()
    {
        return mcc;
    }
    public void setMcc(String mcc)
    {
        this.mcc = mcc;
    }
    public String getIp()
    {
        return ip;
    }
    public void setIp(String ip)
    {
        this.ip = ip;
    }
    public int getPort()
    {
        return port;
    }
    public void setPort(int port)
    {
        this.port = port;
    }
}
